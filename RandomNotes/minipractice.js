"use strict";  

//  ARRAYS - variable that holds multiple values

const fruits = ["apple", "orange", "pear", "banana", "strawberry"];

console.log(fruits[1]);  // orange will print to the console bc it is the 2nd item in the array and arrays start at 0 

// you can also use the length property to find the length of an array . length 

console.log (fruits.length); // 5 will print to the console bc there are 5 items in the array

// you can also replace items 
fruits [0] = "grape"; // this will replace apple with grape

// you can push items to the end of an array

fruits.push("kiwi"); // this will add kiwi to the end of the array 

// you can also .unshift to add items to the beginning of an array and .pop to remove the last item in an array


